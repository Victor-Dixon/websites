const LETTERS = ['A','B','C','D','E','F','G'];
const SIX = ['A','B','C','D','E','F'];

export function calculateDomainScores(responses) {
  const scores = Object.fromEntries(DOMAINS.map(domain => [domain, 0]));

  for (const [qidRaw, answerRaw] of Object.entries(responses || {})) {
    const qid = Number(qidRaw);
    const answer = String(answerRaw?.answer ?? answerRaw).toUpperCase();
    const hit = DOMAIN_KEY[qid]?.[answer];
    if (hit) {
      const [domain, points] = hit;
      scores[domain] += points;
    }
  }

  return scores;
}

export function calculateFlavorVectors(responses) {
  const vectors = {};

  for (const [domain, block] of Object.entries(FLAVOR_BLOCKS)) {
    for (const name of block.names) vectors[name] = 0;
    for (const name of block.names) vectors[`${domain}.${name}`] = 0;
  }

  for (const [qidRaw, answerRaw] of Object.entries(responses || {})) {
    const qid = Number(qidRaw);
    const answer = String(answerRaw?.answer ?? answerRaw).toUpperCase();
    if (!SIX.includes(answer)) continue;

    for (const [domain, block] of Object.entries(FLAVOR_BLOCKS)) {
      const offset = qid - block.start;
      if (offset < 0 || offset > 5) continue;

      const answerIndex = SIX.indexOf(answer);
      const affinity = block.names[(answerIndex + offset) % 6];

      vectors[affinity] += 1;
      vectors[`${domain}.${affinity}`] += 1;
    }
  }

  return vectors;
}

export function determinePrimaryDomain(scores) {
  const entries = Object.entries(scores || {});
  if (entries.length === 0) return null;

  const max = Math.max(...entries.map(([, score]) => score));
  const tied = entries.filter(([, score]) => score === max).map(([domain]) => domain);

  return tied.length === 1 ? tied[0] : tied;
}

export function manifestedDomains(domainScores) {
  const entries = Object.entries(domainScores || {});
  if (entries.length === 0) return [];

  const max = Math.max(...entries.map(([, score]) => score));
  if (max <= 0) return [];

  return entries
    .filter(([, score]) => score === max || score >= max * 0.25)
    .map(([domain]) => domain);
}

export function domainFlavorRange(domain) {
  const normalized = normalizeDomain(domain);
  const block = FLAVOR_BLOCKS[normalized];
  if (!block) return [73, 78];
  return [block.start, block.start + 5];
}

export function currentAdaptiveQuestionIds(responses) {
  const answered = new Set(Object.keys(responses || {}).map(Number));
  const domainIds = Array.from({ length: 36 }, (_, i) => i + 1);
  const missingDomain = domainIds.filter(id => !answered.has(id));
  if (missingDomain.length > 0) return missingDomain;

  const scores = calculateDomainScores(responses);
  const domains = manifestedDomains(scores);
  const flavorIds = [];

  for (const domain of domains) {
    const [start, end] = domainFlavorRange(domain);
    for (let id = start; id <= end; id++) {
      if (!answered.has(id)) flavorIds.push(id);
    }
  }

  return flavorIds;
}

export function adaptiveQuestionIds(responses) {
  return currentAdaptiveQuestionIds(responses);
}

export function adaptiveQuestionsRemaining(responses) {
  return currentAdaptiveQuestionIds(responses);
}

export function adaptiveProgress(responses) {
  const answered = new Set(Object.keys(responses || {}).map(Number));
  const domainAnswered = [...answered].filter(id => id >= 1 && id <= 36).length;

  if (domainAnswered < 36) {
    const remaining_ids = Array.from({ length: 36 }, (_, i) => i + 1).filter(id => !answered.has(id));
    return {
      phase: 'domain',
      total: 36,
      answered: domainAnswered,
      remaining: remaining_ids.length,
      remaining_ids,
      complete: false
    };
  }

  const scores = calculateDomainScores(responses);
  const domains = manifestedDomains(scores);
  const expectedFlavorIds = [];

  for (const domain of domains) {
    const [start, end] = domainFlavorRange(domain);
    for (let id = start; id <= end; id++) expectedFlavorIds.push(id);
  }

  const flavorAnswered = expectedFlavorIds.filter(id => answered.has(id)).length;
  const remaining_ids = expectedFlavorIds.filter(id => !answered.has(id));

  return {
    phase: remaining_ids.length ? 'flavor' : 'complete',
    total: 36 + expectedFlavorIds.length,
    answered: 36 + flavorAnswered,
    remaining: remaining_ids.length,
    remaining_ids,
    complete: remaining_ids.length === 0
  };
}

export function determineTier(score) {
  return tierFromScore(score);
}

export function tierFromScore(score) {
  if (score >= 35) return 5;
  if (score >= 24) return 4;
  if (score >= 16) return 3;
  if (score >= 8) return 2;
  if (score >= 1) return 1;
  return 0;
}

export function calculatePercentile(input, secondHighestTier = 0, powerCountTerm = 0) {
  if (typeof input === 'object' && input !== null) {
    return 70 + ((input.highestTier || 0) * 2.5) + ((input.secondHighestTier || 0) * 1) + (input.powerCountTerm || 0);
  }

  const primaryScore = Number(input || 0);
  const highestTier = tierFromScore(primaryScore);
  return 70 + (highestTier * 2.5) + (secondHighestTier * 1) + powerCountTerm;
}

export function determineThreatClass(tier, flavorVectors = {}) {
  const strategic = Object.keys(flavorVectors).some(key => STRATEGIC_THREAT_POWERS.has(key.split('.').pop()) && flavorVectors[key] > 0);
  if (strategic) return 'Strategic Threat';
  if (tier >= 5) return 'Cataclysm';
  if (tier >= 4) return 'High Paragon';
  if (tier >= 3) return 'Paragon';
  if (tier >= 2) return 'Elevated';
  return 'Standard';
}

function extractResponseMap(input) {
  if (!input) return {};
  if (input.responses && typeof input.responses === 'object') return input.responses;
  return input;
}

export function buildCharacterSheet(responses) {
  const responseMap = extractResponseMap(responses);
  const domain_scores = calculateDomainScores(responseMap);
  const manifested_domains = manifestedDomains(domain_scores);
  const maxScore = Math.max(...Object.values(domain_scores));
  const domainTiers = Object.fromEntries(Object.entries(domain_scores).map(([domain, score]) => [domain, tierFromScore(score)]));
  const sortedTiers = Object.values(domainTiers).sort((a, b) => b - a);
  const flavor_vectors = calculateFlavorVectors(responseMap);
  const primary_domain = determinePrimaryDomain(domain_scores);
  const highestTier = sortedTiers[0] || 0;
  const secondHighestTier = sortedTiers[1] || 0;
  const powerCountTerm = manifested_domains.length;
  const percentile = calculatePercentile({ highestTier, secondHighestTier, powerCountTerm });
  const threat_class = determineThreatClass(highestTier, flavor_vectors);

  return {
    version: 'spark-protocol-v5',
    primary_domain,
    manifested_domains,
    domain_scores,
    domain_tiers: domainTiers,
    highest_score: maxScore,
    tier: highestTier,
    percentile,
    threat_class,
    flavor_vectors,
    character_packet: {
      schema_version: 'spark_character_sheet_v1',
      version: 'spark_character_sheet_v1',
      ruleset: 'Spark Protocol v5',
      percentile,
      threat_class,
      domains: manifested_domains.map(domain => ({
        name: domain,
        score: domain_scores[domain],
        tier: domainTiers[domain]
      }))
    }
  };
}

export function buildComicProfile(sheet) {
  return buildAegisProfile(sheet);
}

export function buildAegisProfile(sheet) {
  return {
    cover_line: `A ${String(sheet.primary_domain).toUpperCase()}-type emergence ranked in the ${sheet.percentile}th percentile with ${sheet.threat_class} threat-band behavior.`,
    sections: [
      `Primary Manifestation: ${String(sheet.primary_domain).toUpperCase()}`,
      `Percentile: ${sheet.percentile}`,
      `Threat Class: ${sheet.threat_class}`
    ],
    sheet
  };
}

export function normalizeDomain(domain) {
  const d = String(domain || '').toLowerCase();
  const aliases = {
    bulwark: 'titan',
    inferno: 'energy',
    shadow: 'specter',
    control: 'omni',
    wild: 'primal'
  };
  return aliases[d] || d;
}
